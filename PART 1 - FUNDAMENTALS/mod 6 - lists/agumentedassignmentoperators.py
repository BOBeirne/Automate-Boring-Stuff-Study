spam = 42

spam += 1 #same as spam = spam + 1
print(spam)

#works also for
spam -= 1
print(spam)
spam *= 1
print(spam)
spam /= 1
print(spam)
spam %= 1
print(spam)

# also works on strings
spam = 'Hello'
spam += ' World' #  same as spam = spam + world
print(spam)

bacon = ['zophie']
bacon *= 3 # same as bacon = bacon * 3
print(bacon)